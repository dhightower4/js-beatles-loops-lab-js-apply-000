function theBeatlesPlay(musicains, instruments) {
  var statements = []
  for (let i = 0; i < musicians.length; i++) {
    statements.push(`${musicians[i]} plays the ${instruments[i]}`)
  }
  return (statements)
}

function johnLennonFacts(facts) {
  let i = 0;
  while (i < facts.length) {
    facts[i] = (`${facts[i]} + !!!`)
    i++
  }
}
